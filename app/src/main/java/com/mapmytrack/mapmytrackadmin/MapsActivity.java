    private void makeMap() {

        db.collection("User Locations").addSnapshotListener(new EventListener<QuerySnapshot>() {
            @Override
            public void onEvent(@Nullable QuerySnapshot snapshot, @Nullable FirebaseFirestoreException e) {

                GeoPoint geoPoint = (GeoPoint) snapshot.getDocuments().get(0).getData().get("geo_point");
                String name = (String) snapshot.getDocuments().get(0).getData().get("name");

                LatLng position = new LatLng(geoPoint.getLatitude(), geoPoint.getLongitude());

                MarkerOptions markerOptions = new MarkerOptions();
                markerOptions.position(position);
                markerOptions.title("Current Position");
                markerOptions.icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_MAGENTA));
                list.add(position);
                drawPolyLineOnMap(list);


                //move map camera
                mMap.moveCamera(CameraUpdateFactory.newLatLng(position));
                mMap.addMarker(new MarkerOptions().position(position).title(name));
            }
        });

    }


    /**
     * Draw polylines
     * @param list
     */
    private void drawPolyLineOnMap(List<LatLng> list) {
        PolylineOptions polyOptions = new PolylineOptions();
        polyOptions.color(Color.BLUE);
        polyOptions.width(10);
        polyOptions.addAll(list);
        mMap.clear();
        mMap.addPolyline(polyOptions);
        LatLngBounds.Builder builder = new LatLngBounds.Builder();
        for (LatLng latLng : list) {
            builder.include(latLng);
        }
        final LatLngBounds bounds = builder.build();
        //BOUND_PADDING is an int to specify padding of bound.. try 100.
        CameraUpdate cu = CameraUpdateFactory.newLatLngBounds(bounds, 20);
        mMap.animateCamera(cu);
    }

